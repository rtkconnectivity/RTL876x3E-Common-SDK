/** @defgroup BAS Battery Service
  * @brief  Battery service
   * @details

     The Battery Service exposes the state of a battery within a device.

     Battery Service generally makes up a profile with some other services, and it can provide the state of a battery within a device.

     The default supported feature provided by BAS is the notify property of battery level characteristic,
     and the application developers can modify the feature supported by BAS according to their own requirements.
     The specific configuration process can be achieved by modifying file @ref bas_config.h.

    * <b>Example usage</b>
    * \code{.c}

         #define BAS_BATTERY_LEVEL_NOTIFY_SUPPORT    1

    * \endcode

     Applications shall register battery service during initialization through @ref bas_add_service function.

     Applications can set the battery level of BAS through @ref bas_set_parameter function.

     Applications can send the battery level value of BAS to the client with a notification through @ref bas_battery_level_value_notify function.

  * @{
  */

/*============================================================================*
 *                         Macros
 *============================================================================*/
/** @defgroup BAS_Exported_Macros BAS Exported Macros
  * @brief
  * @{
  */

#define BAS_READ_BATTERY_LEVEL  1             /**< Read battery level value index. */

#define BAS_NOTIFY_BATTERY_LEVEL_ENABLE  1    /**< Battery level notification enabled index. */
#define BAS_NOTIFY_BATTERY_LEVEL_DISABLE  2   /**< Battery level notification disabled index. */

/** End of BAS_Exported_Macros
* @}
*/

/*============================================================================*
 *                         Types
 *============================================================================*/
/** @defgroup BAS_Exported_Types BAS Exported Types
  * @brief
  * @{
  */

/**
*  @brief Battery service parameter type
*/
typedef enum
{
    BAS_PARAM_BATTERY_LEVEL = 0x01, /**< Battery level value. Write only. Size is 1 octet. Range is 0 to 100.
                                         Unit is org.bluetooth.unit.percentage. 0% represents fully discharged;
                                         100% represents fully charged. Default value is 0. */
} T_BAS_PARAM_TYPE;

/**
*  @brief Set battery service parameter upstream message data
*/
typedef union
{
    uint8_t notification_indification_index;
    uint8_t read_value_index;
} T_BAS_UPSTREAM_MSG_DATA;

/**
*  @brief Set battery service parameter upstream callback data
*/
typedef struct
{
    uint16_t                conn_handle;
    uint16_t                cid;
    uint8_t                 conn_id;
    T_SERVICE_CALLBACK_TYPE msg_type;
    T_BAS_UPSTREAM_MSG_DATA msg_data;
} T_BAS_CALLBACK_DATA;

/** End of BAS_Exported_Types
* @}
*/


/*============================================================================*
 *                         Functions
 *============================================================================*/
/** @defgroup BAS_Exported_Functions BAS Exported Functions
  * @brief
  * @{
  */

/**
  * @brief       Add battery service to the Bluetooth Host.
  *
  * @param[in]   p_func  Callback when service attribute is read, written or CCCD updated.
  *
  * @return Service ID generated by the Bluetooth Host: @ref T_SERVER_ID.
  * @retval 0xFF Operation failure.
  * @retval Others Service ID assigned by Bluetooth Host.
  *
  * <b>Example usage</b>
  * \code{.c}
     void profile_init()
     {
         server_init(service_num);
         bas_id = bas_add_service(app_handle_profile_message);
     }
  * \endcode
  */
T_SERVER_ID bas_add_service(void *p_func);

/**
 * @brief       Set a battery service parameter.
 *
 * This function can be called with a BAS parameter type @ref T_BAS_PARAM_TYPE and it will set the BAS parameter.
 * The 'p_value' field must point to an appropriate data type that meets the requirements for the corresponding parameter type.
 * (For example, if required data length for parameter type is 1 octets, p_value should be cast to a pointer of uint8_t.)
 *
 * @param[in]   param_type   Battery service parameter type: @ref T_BAS_PARAM_TYPE.
 * @param[in]   length       Length of data to write.
 * @param[in]   p_value Pointer to data to write.
 *
 * @return Operation result.
 * @retval true Operation success.
 * @retval false Operation failure.
 *
 * <b>Example usage</b>
 * \code{.c}
    void test(void)
    {
        bool ret = bas_set_parameter(BAS_PARAM_BATTERY_LEVEL, 1, &battery_level);
    }
 * \endcode
 */
bool bas_set_parameter(T_BAS_PARAM_TYPE param_type, uint8_t length, uint8_t *p_value);


/**
 * @brief       Send notify battery level notification data.
 *
 *
 * @param[in]   conn_id        Connection ID.
 * @param[in]   service_id     Service ID.
 * @param[in]   battery_level  The current charge level of a battery.
                               Range is 0 to 100. Unit is org.bluetooth.unit.percentage.
                               0% represents fully discharged; 100% represents fully charged.
 * @return Operation result.
 * @retval true Operation success.
 * @retval false Operation failure.
 *
 * <b>Example usage</b>
 * \code{.c}
    void test(void)
    {
        bool ret = bas_battery_level_value_notify(conn_id, bas_id, battery_level);
    }
 * \endcode
 */
bool bas_battery_level_value_notify(uint8_t conn_id, T_SERVER_ID service_id, uint8_t battery_level);


/**
 * @brief       Confirm read battery level value request.
 *
 *
 * @param[in]   conn_id        Connection ID.
 * @param[in]   service_id     Service ID.
 * @param[in]   battery_level  The current charge level of a battery.
                               Range is 0 to 100. Unit is org.bluetooth.unit.percentage.
                               0% represents fully discharged; 100% represents fully charged.

 * @return Operation result.
 * @retval true Operation success.
 * @retval false Operation failure.
 *
 * <b>Example usage</b>
 * \code{.c}
    void test(void)
    {
        bool ret = bas_battery_level_value_read_confirm(conn_id, bas_id, battery_level);
    }
 * \endcode
 */
bool bas_battery_level_value_read_confirm(uint8_t conn_id, T_SERVER_ID service_id,
                                          uint8_t battery_level);

/** @} End of BAS_Exported_Functions */

/** @} End of BAS */


